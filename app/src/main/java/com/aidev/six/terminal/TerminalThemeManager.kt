    fun apply(session: TerminalSession, preset: TerminalThemePreset) {
        try {
            val emu = session.javaClass.getMethod("getEmulator").invoke(session) ?: return
            val colors = emu.javaClass.getField("mColors").get(emu)
            val currentColors = colors.javaClass.getField("mCurrentColors").get(colors) as IntArray
            for (i in 0 until 16) {
                currentColors[i] = preset.colors[i]
            }
            currentColors[TextStyle.COLOR_INDEX_FOREGROUND] = preset.colors[16]
            currentColors[TextStyle.COLOR_INDEX_BACKGROUND] = preset.colors[17]
            currentColors[TextStyle.COLOR_INDEX_CURSOR] = preset.colors[18]
            session.onColorsChanged()
        } catch (e: Exception) {
            Log.e(TAG, "Failed to apply theme", e)
        }
    }

    fun applyByName(session: TerminalSession, themeName: String) {
        val preset = presets[themeName] ?: return
        apply(session, preset)
    }

    fun applyBackgroundOverride(session: TerminalSession, bgColor: Int) {
        try {
            val emu = session.javaClass.getMethod("getEmulator").invoke(session)
            val colors = emu.javaClass.getField("mColors").get(emu)
            val currentColors = colors.javaClass.getField("mCurrentColors").get(colors) as IntArray
            currentColors[TextStyle.COLOR_INDEX_BACKGROUND] = bgColor or (0xFF shl 24)
            session.onColorsChanged()
        } catch (e: Exception) {
            Log.e(TAG, "Failed to apply background override", e)
        }
    }
