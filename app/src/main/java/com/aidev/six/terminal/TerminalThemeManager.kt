@Immutable
data class TerminalThemePreset(
    val name: String,
    val label: String,
    val colors: IntArray,
) {
    override fun equals(other: Any?): Boolean {
        if (this === other) return true
        if (other !is TerminalThemePreset) return false
        return name == other.name && label == other.label && colors.contentEquals(other.colors)
    }
    override fun hashCode(): Int = 31 * name.hashCode() + label.hashCode() + colors.contentHashCode()
}

